package model;

public class SecretQuestion {
	
	private int SQID;
	private String question;
	
	public SecretQuestion() {
		
	}
	
	public int getSQID() {
		return SQID;
	}
	
	public void setSQID(int SQID) {
		this.SQID = SQID;
	}
	
	public String getQuestion() {
		return question;
	}
	
	public void setQuestion(String question) {
		this.question = question;
	}
	
	

}
